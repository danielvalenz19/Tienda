    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String des = lblDescripcion.getText();
        String cat = cbxCategoria.getSelectedItem().toString();
        int cant = Integer.parseInt(lblCantidad.getText());
        float costo = Float.parseFloat(lblCosto.getText());
        float precio = Float.parseFloat(lblPrecio.getText());
        String est;
        if (chbxEstado.isSelected()) {
            est = "S";
        } else {
            est = "N";
        }
        CP.insertDatos(des, cat, cant, costo, precio, est);

    }//GEN-LAST:event_btnGuardarActionPerformed


